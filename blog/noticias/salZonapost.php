        <h3 class="h3subMenuNota">Con éxito en taquilla, SalZona 2017 logró complacer a sus asistentes</h3>
        <p class="pieNoticiaNota">Julio 2017</p>        
        
        <p>Con el total de boletos vendidos, la tercera edición de SalZona, 2017, realizado este sábado 1 de julio, a las 19:00 hrs., en el Gimnasio Universitario “Nido del Halcón”, del Campus CAD, logró complacer a jóvenes y adultos, quienes en pareja, entre amigos, con familiares, solos y como les fue posible, disfrutaron de una entusiasta velada al compás de la música y el ritmo de la salsa.</p>        
        
        <div class="div1Notas">
            <p>Desde la primera canción del evento, “Cachita”, a cargo del grupo Mayabe, hasta la última presentación del mismo, con JJ Ruiz, los asistentes de SalZona 2017, con ayuda de la Xalli Big Band, se encargaron de mantener completamente llena, durante todo la gala, la pista de baile en el “Nido del Halcón”; frente a un público variado, la cartelera de la tercera edición de SalZona, incluidas las academias de baile, lograron complacer los distintos y exquisitos gustos repartidos entre la audiencia.</p>
            <p>Un evento de tal magnitud, en el que más de mil 200 personas fueron acogidas, requirió aproximadamente 70 personas en las distintas áreas de servicio que enmarcaron el evento; desde la instalación de mesas y sillas, la iluminación y el sonido, hasta los acomodadores, la vigilancia y los meseros, estuvieron conformados por personal del Campus CAD, servicio que aportó calidad y esfuerzo para que SalZona 2017 se condujera con éxito.</p>
            <p>Respecto al evento, Aram Jesús, cofundador del grupo Mayabe, por tercera vez en SalZona, resaltó que esta edición es ejemplifica el gusto que la gente tiene por la salsa; “la FUV le apuesta a un género que se había perdido y en cada edición ha tenido una visión más amplia, con expectativas más altas, desde la publicidad, hasta traer un cantante de talla internacional, como es JJ Ruiz; estoy seguro que el siguiente año habrá también más expectativas”.</p>
