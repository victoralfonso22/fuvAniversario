    <p class="h3subMenu">Su impacto social</p>
    
    
    <p>Desafortunadamente, el 50 por ciento de los ingresos hospitalarios por cáncer, en el Estado de Veracruz, es maligno, y por día, se registran 16 decesos de pacientes veracruzanos. Frente a esta lamentable situación, sólo se cuenta, en el estado de Veracruz, con un equipo de radioterapia de alta tecnología, como el que la FUV-UV-IIMB adquiere, el cual sólo logra atender a un millón de personas, de las ocho millones que habitan en la entidad. Con el proyecto “Urgencia de equipo, urgencia de vida” se contemplan, al año, 17 mil 500 sesiones de radioterapia, 450 simulaciones de tratamientos y 800 tomografías.</p>
    <p>Asimismo, un equipo con estas características representa para la UV, diversas posibilidades de generación y transmisión de conocimiento, sobre todo para el área de ciencias de la salud. La UV cuenta, además, con personal calificado que permite la asistencia, operación y seguimiento del proyecto; atender de manera permanente este equipo implicaría nuevo personal altamente capacitado y especializado como parte de la plantilla laboral al servicio de la UV. Se generaría investigación y casos de estudio: con la docencia se transmitiría este conocimiento y mediante el estudiantado se aplicaría.</p>
    <p>Con este programa, la UV sería la segunda universidad pública en el país, con alta capacidad de operación de equipo de radioterapia y personal calificado. Se ampliaría el panorama de acciones académicas e investigación; habría inclusión en la red temática de física médica de Conacyt; habría la oportunidad de abrir la especialidad de radioterapia y campos clínicos para las diversas facultades del área de salud, así como la generación y la procuración de fondos para nuevos proyectos.</p>
    <p>Dentro de la UV, la gestión del proyecto requirió también la participación de Dirección de Proyectos, Construcciones y Mantenimiento, para aspectos que conciernen a la supervisión de obra; de la Dirección de Control Patrimonial, para cuestiones de la propiedad de equipo de la UV; de la Dirección de Recursos Humanos, para la contratación de personal, y de la Secretaría de Administración y Finanzas, para asuntos relativos a pagos y reporteo. Esta colaboración expresa la madurez institucional entre UV y FUV, la cual permitirá que el proyecto “Urgencia de equipo, urgencia de vida” ahora materializado en el “Centro de Atención para el Cáncer” pueda inaugurarse en 2017.</p>
    
    
    
