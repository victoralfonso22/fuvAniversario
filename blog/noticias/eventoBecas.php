        <h3 class="h3subMenuNota">“Hacer una carrera es como un proceso de parto y el apoyo de la FUV es como el acompañamiento que una madre requiere en dicho proceso”: Sunashi Sashell</h3>
        <p class="pieNoticiaNota">Mayo de 2017</p>
        <br />
        
        
        <p>Estudiante de octavo semestre de la Licenciatura en Enfermería de la Universidad Veracruzana, en la región Veracruz-Boca del Río, y becaria de la Fundación UV, desde su segundo semestre, Sunashi Sashell Barrera Alvarado entiende la relación entre padrino y becario como el acompañamiento que una madre requiere en su proceso de parto, como ella expresa: “mi tesis es acerca de este acompañamiento continuo que la mujer embarazada recibe en el parto y lo relaciono con lo que hacen los benefactores de la Fundación UV, pues con su aportación nos acompañan, nos hacen sentir seguros y nos dan fuerza para cumplir con nuestras metas a lo largo de nuestra carrera”.</p>
        <p>Para Sunashi, madre de una adolescente de 14 años, así como el proceso de parto presenta al paciente miedos y obstáculos, también el cursar una carrera conlleva dificultades que hay que enfrentar y superar. “Mi principal temor estuvo relacionado con mi hija y mi esposo. Estaba acostumbrada a tenerlos cerca y fue muy difícil dejarlos en Matías Romero (municipio perteneciente al estado de Oaxaca). Uno sabe que en algún momento tendrá que dejar a sus papás, pero dejar a tus hijos y a tu esposo, hace que te preguntes: ¿qué va a pasar con ella, con él, con la relación madre e hija, con la relación de pareja? A veces pasaba un mes o dos sin poder visitarlos, y a veces debía esperar hasta el siguiente periodo vacacional. Lo anterior, sin embargo, me obligó a terminar antes los créditos de la carrera: en tres años y medio”.</p> 
        
        <div class="div1Notas">
            <p>“Si hacer mi carrera fuera un proceso de parto, el resultado sería el ejemplo que le doy a mi hija al tener una beca que me exige constancia y un buen promedio. Quise darle a ella ese ejemplo y quiero también poder retribuir este compromiso a otros compañeros, en un futuro; yo en algún momento tengo que aportar y brindar a otros la ayuda que ahora me toca recibir.” </p>
            <p>Sunashi Sashell emitió el discurso de agradecimiento, durante la “12 Entrega de Becas del Comité Regional Veracruz-Boca del Río”, celebrado el pasado 8 de mayo, en las instalaciones de Grupo Tony, evento que hizo entrega de 100 becas, un monto de 480 mil pesos, a 72 mujeres y 28 hombres, para el periodo febrero-julio 2017. Desde el inicio de operación, en 2011, a la fecha, el Comité Regional de Becas Veracruz-Boca del Río, ha podido entregar mil 264 becas, un equivalente de 6 millones 67 mil 200 pesos, para 439 beneficiarios, 307 son mujeres y 132 hombres.</p>
            <p>La “12ª. Entrega de Becas del Comité Regional Veracruz-Boca del Río”, contó con la presencia de la Rectora de la Universidad Veracruzana, Dra. Sara Ladrón de Guevara, del Vicerrector de la Región, Dr. Alfonso Gerardo Pérez Morales, de la vicepresidenta de la Fundación Tony, Marilú Chedraui Bolado y del presidente de la Junta Directiva de la Fundación UV, Víctor Aguilar Pizarro; además de Asociados de la Fundación UV, Vocales del Comité de Becas, Padrinos, miembros de la sociedad general e integrantes de la comunidad universitaria, quienes reiteraron su compromiso para evitar que la situación económica de los estudiantes les obligue a abandonar sus estudios.</p>
            
            
        
