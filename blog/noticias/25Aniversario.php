        <p>Es el caso del proyecto Quiahuiztlán–Villa Rica, que permitió restaurar la arqueología totonaca para entender la historia de esta región; los servicios que, en atención al vínculo de la UV con el Banco Interamericano de Desarrollo, se prestaron al Programa de Cooperación Técnica Intrarregional, y que permitieron intercambiar conocimientos en ecología para el desarrollo regional; eventos que son antecedente de lo que hoy día es una sólida alianza con la cultura, como es el primer Concierto de Gala con la Orquesta Sinfónica de Xalapa, y un Homenaje al Mtro. Agustín Lara, en el Teatro de la Reforma de Veracruz. Asimismo, el programa de becas logró atacar la deserción escolar mediante la campaña “Ex Alumnos de la UV”, que logró visitar mil 203 egresados.</p>
        
        <p class="h3subMenu">Etapa de crecimiento: 1998-2006</p> 
        <p>En su segunda etapa, de 1998 a 2006, identificada como de crecimiento, las labores de la FUV se caracterizaron por la administración tanto de instalaciones universitarias como de proyectos especiales. La administración de instalaciones inició con la firma del comodato del Campus para la Cultura,las Artes y el Deporte en Xalapa (CCAD), superficie de más de 30 hectáreas que, desde entonces, han permitido fomentar la salud y el desarrollo integral mediante Centros de Formación Deportiva, como son los de Fútbol Soccer y de Baloncesto y que al día de hoy se encuentran activos. En este mismo periodo, se recibió en comodato la Unidad Deportiva de la Facultad de Educación Física de la UV, en Boca del Río, y el Gimnasio, mejor conocido como “nido de los Halcones”, y que es sede de los representativos de la UV.</p>
        <p>Por su parte, son casos de administración de proyectos especiales con alto impacto social, la “Casa de la Universidad: Vecinos del Manglar”, que materializó programas de educación ambiental, acciones de reforestación y cuidado del mangle; el 13º Congreso Bienal de la Organización Universitaria Interamericana (OUI), “Educación Superior, Integración Continental y Desarrollo Humano” celebrado en Veracruz, que reunió a más de mil representantes de 400 universidades del continente americano; y el Programa Universitario de Apoyo al Empleo (PUAE), que capacitó a más de 16 mil personas de las cinco regiones, con herramientas prácticas de liderazgo, mediante el vínculo entre sectores productivos (micro, pequeño y mediano)  y academia (docentes y estudiantes de la UV).</p>
        <p>Sobresale el apoyo a eventos como, “Otilia Rauda”, película basada en la obra de Sergio Galindo, dirigida por Arturo Ripstein, de la cual pudo editarse una memoria gráfica y cuya premier se realizó en beneficio de los programas de la FUV, además de realizarse en el marzo de su 10º aniversario; la producción del espectáculo “Jarocho”, cuya premier también fue en beneficio de nuestros programas; y la co-producción de los documentales “Grandes de Tlacotalpan. Entre décimas y sones” y “Ernesto Cardenal. Solentiname”, el primero, un disco y un documental de los más grandes exponentes de nuestro son, mientras que el segundo compartió la vida y obra del poeta nicaragüense, Doctor Honoris Causa por nuestra máxima casa de estudios. En este periodo, el programa de becas incrementó su cobertura, gracias al donativo de la Fundación Miguel Alemán, el cual fue etiquetado para apoyar estudiantes de excelencia académica y escasos recursos.</p>

        <p class="h3subMenu">Etapa de desarrollo: 2007-hoy día</p> 
        <p>En su etapa de desarrollo, de 2007 a la fecha, las labores de la FUV, muestran mayor madurez institucional, no sólo en sus procesos administrativos y operativos, sino en su objeto social. Ejemplo de ello son, la acreditación en “Institucionalidad y Transparencia” de parte del Centro Mexicano para la Filantropía (CEMEFI), el cual realizó una minuciosa revisión documental y financiera para asentar con nivel óptimo, que las actividades desarrolladas, efectivamente son en beneficio de la sociedad (este distintivo se conserva hasta hoy día), y la autorización de la Secretaría de Hacienda y Crédito Público (SHCP), para recibir donativos internacionales.</p>
        <p>En este periodo se realiza el Sorteo UV, el cual es liderado por la UV y ofrece a cualquier individuo la oportunidad de apoyar directamente a los estudiantes, al tiempo de retribuir un poco de lo mucho que la UV ha brindado. Sobresale, también, la creación del Consejo Estatal de Becas, que concentra la actividad de procuración de fondos en Comités Regionales, de los cuales, al día de hoy, operan con fortaleza los comités de Veracruz-Boca del Río, Xalapa y Coatzacoalcos-Minatitlán, mientras que los de Poza Rica-Tuxpan y Orizaba-Córdoba, se encuentran en gestión.</p>
      



<p>Asimismo, solicita la UV, la firma de dos nuevos comodatos: el Campus Ixtaczoquitlán, administrado hasta la fecha en coordinación con las autoridades de la región Orizaba-Córdoba; y Tlaqná. Centro Cultural, complejo que se ha venido administrando y que será sujeto de un Programa Integral de Comercialización, a fin de convertirle en uno de los espacios culturales más activos e importantes del país y continente. </p>
<p>En cuanto a la administración de proyecto, sobresalen, “Manejo integrado de roedores en agro-sistemas cañeros”, liderado por la Facultad de Ciencias Biológicas y Agropecuarias de Peñuela, Campus Orizaba-Córdoba, y el “Proyecto integral de ordenamiento para las pesquerías del Golfo de México”, del Instituto de Ciencias Marinas y Pesquerías, el cual impartió talleres en los estados del Golfo y Mar Caribe, para regular y administrar de manera sustentable los recursos pesqueros. </p>

        
        <p>Los proyectos que actualmente sobresalen son, el programa “Crea e Innova”, que permite nuevas maneras de analizar y resolver situaciones a través de la metodología registrada “LEGO Serious Play”, y el Diplomado en Desarrollo de Talentos Directivos, que plantea nuevos paradigmas de trabajo que permitan adaptar nuevos comportamientos y modelos de aprendizaje, y el cual está próximo a lanzarse.</p>
        <p>Próximo a inaugurarse bajo el nombre de “Centro de Atención para el Cáncer”, se encuentra el proyecto “Urgencia de Equipo, Urgencia de Vida”. Este proyecto a gran escala tanto en la obtención de recursos, como en su gestión y transparencia, inició en 2012, y hoy día ofrece la oportunidad, mediante la adquisición de un acelerador lineal y de un sistema de tomografía, de realizar 17 mil 500 sesiones de radioterapia, además de 450 simulaciones de tratamientos y 800 tomografías anualmente.</p>
        <p>Para la UV, este “Urgencia de equipo, urgencia de vida” significa no sólo el fortalecimiento de su vínculo con la sociedad, sino el de su investigación, en el área de ciencias de la salud, el de su plantilla académica y el de su capacitación al estudiantado; para la FUV, es un reflejo de madurez institucional que conlleva un crecimiento en sus labores administrativas y operativas, así como el cumplimiento de su objeto social.</p>
