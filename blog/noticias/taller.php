        <h3 class="h3subMenuNota">Campus CAD: sede del “1er Taller de Fotografía de Flora y Fauna”</h3>
        <p class="pieNoticiaNota">Abril de 2017</p>
        <br />
        
        
        <p>A partir del conocimiento técnico en el uso de cámara y al manejo de flora y fauna, el “1er Taller de Fotografía de Flora y Fauna” logró plasmar la creatividad e inquietud de sus asistentes en las más de 70 especies que, hasta hoy día, el Campus CAD tiene registradas: 48 especies de aves, 10 especies de serpientes y 8 especies de mamíferos.</p>
        <p >En palabras del organizador del taller, Pablo Cobos Mejía, biólogo responsable de la Unidad de Manejo de Fauna del CCAD, “el taller -el cual se realizó el pasado 25 y 26 de marzo- buscó sentar las bases para que los asistentes aprendieran a fotografiar correctamente flora y fauna, sea para tener una buena foto de sus animales caseros o de las flores de su jardín, sea para concursar en alguna revista especializada o para subirlas a internet”.</p> 
        <div class="div1Notas">
            
            <p>Por su parte, Pedro Alberto Ríos, responsable de impartir a los asistentes los aspectos técnicos en el uso de la cámara, mencionó que “la fotografía de flora permite aprender a dominar la luz con un sujeto estático, mientras que la fotografía de mamíferos, como no controlamos el comportamiento del animal, es más difícil. Sin embargo, la fotografía de aves e insectos, son las que más se complican; en aves porque son sujetos que se mueven a gran velocidad, lo que convierte en una hazaña enfocarlos en movimiento, y en insectos porque, al trabajar a pequeñas distancias, el mínimo movimiento del fotógrafo puede verse reflejado en la imagen”.</p>
        
