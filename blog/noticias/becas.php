        <p class="h3subMenu">Diferentes estrategias para un mismo fin</p> 
        
        <p><i>Campaña ex alumnos.</i> El proyecto comenzó en 1994. La estrategia fue localizar y establecer contacto con todos aquellos que tuvieron la oportunidad de estudiar en la UV. La difusión de este proyecto consistió en visitas a las facultades y llamadas telefónicas. Los fondos que se obtuvieran eran destinados a estudiantes de buen desempeño académico y vulnerabilidad económica.</p>
        <p>Para 1995, se habían visitado mil 203 ex alumnos, de los cuales, 247 fueron donadores, 20.5 por ciento del total. La asignación de la beca consistía en llenar una solicitud en donde se requería un promedio mínimo de nueve; una vez que las solicitudes eran aprobadas, se realizaba a cada solicitante un estudio socio-económico. Para 1996, la campaña ex alumnos había conseguido 805 donativos. En 1999, un becario de este programa, de la carrera de Mecatrónica Eléctrica de la Facultad de Ingeniería de la UV, región Veracruz- Boca del Río, recibió el Premio Nacional de la Juventud 1999. </p>
        <p>En 2007, la campaña recibió el nombre de “Amigos y ex alumnos de la UV”, el cual otorgó 84 becas. En 2008, modificó su esquema y en lugar de un promedio de 9, solicitó un promedio mínimo de 8, al tanto que la vulnerabilidad económica puede afectar el desempeño académico, aun así el 83 por ciento de los becarios, en este año, aumentó su promedio a 9.</p>
        <p><i>Adopta un becario.</i> Este programa, vigente al día de hoy, se formalizó en 2000. Su estrategia inicial consistió en que los asociados de la Fundación adoptaran, por lo menos, un becario. Incluso cada asociado promovió el programa e invitaron a otros posibles donantes. En 2006, mediante la creación del Programa de Becas Fundación UV, Adopta un becario modificó su esquema: además de las donaciones de los asociados se aceptaron las de cualquier otra persona o institución. Esta modalidad logró apoyar 50 estudiantes de nivel licenciatura distribuidos en las cinco regiones de la UV. Para 2008, logró incrementar el número de becas otorgadas a 91. También en 2008, modificó su esquema: además de estudiantes de buen desempeño, el programa se destinó a estudiantes regulares con adaptabilidad laboral, y se le dio al donante la oportunidad de elegir la región, la carrera y el estudiante que deseara apoyar. Para 2016, se lograron entregar 426 becas a estudiantes con vulnerabilidad
económica y buen desempeño académico en las 5 regiones universitarias.
</p>
<p><i>Incentivos académicos.</i> Esta estrategia consiste en permitir que los estudiantes reciban estímulos por su buen desempeño. Resaltan los siguientes. En 2001, se entregaron apoyos para estudios de posgrado, así como para congresos o eventos académicos. En 2003, se hizo entrega de estímulos a 37 deportistas que obtuvieron medallas, este mismo año, en la Universiada Nacional. En 2011, se otorgaron 81 Becas-Trabajo, las cuales permitieron que estudiantes colaboren, mediante el conocimiento adquirido en aulas, en las oficinas administrativas, instalaciones deportivas y talleres de capacitación de la Fundación. En 2012, se creó la campaña “Siempre universitarios”, la cual consistió en micro donativos, destinados a otorgar estímulos académicos y deportivos a estudiantes de la UV, campaña que actualmente continúa. </p>

<br /> 



<p>En 2016, el programa Becas de la Fundación UV, el cual incluye tanto Adopta un becario, como los Micro-donativos “Siempre universitarios”, logró captar un total de 345 donantes, gracias a los cuales se pudieron entregar, en ese mismo año, 410 becas, y beneficiar con estímulos y apoyos económicos a 197 estudiantes.</p>
<p>Como pudo apreciarse, el programa Becas de la Fundación UV, con el objetivo de ayudar al estudiante en su paso por la universidad, atiende una misma problemática desde distintas perspectivas: procurar que sus necesidades más básicas estén cubiertas y evitar así que su desempeño se vea afectado o que decida desertar de la universidad. </p>



