        <h3 class="h3subMenuNota">5 razones para donar</h3>
        <p class="pieNoticiaNota">Junio 2017</p>
        
        
        <p>El acto de donar no cuenta con muy buena reputación. Suele verse, en el mejor de los casos, como un acto que, si bien no nos quita, tampoco nos beneficia; esto es, como un acto completamente desinteresado y con el cual no podría obtenerse ninguna ganancia. Hay, sin embargo, algunas razones  que podrían probar lo contrario y que quizá permitan replantear nuestro entendido frente al acto de donar.</p>
        
        <p class="h3subMenu">1.- Donar es como comer pastel de chocolate</p>
        <p>Un estudio basado en imágenes de resonancia magnética, a cargo de científicos de la Universidad de Stanford y la Universidad de Harvard, demostró que el acto de generosidad despierta la misma parte del cerebro que responde a la comida y el sexo.</p>
        <p>Esta investigación mostró que la bondad o la amabilidad es como un “chocolate psicológico”, como Jamail Zaki señala: “cuando los participantes en la prueba actuaron de manera generosa, en ellos se activó la corteza orbitofrontal del cerebro, zona que registra y da valor a las recompensas”.</p>
        <p>Se mostró también que la generosidad reduce el estrés, ayuda a la longevidad y produce la hormona de la felicidad: dopamina. Cuando en la prueba los participantes ayudaron a otros, se sintieron más enérgicos y fuertes, e incluso experimentaron una sensación de euforia o de estar “drogados de ayuda” (<i>“helper's high”</i>), término usado por los voluntarios y que Allan Luks confirmó después de analizar los efectos de 3 mil voluntarios (hombres y mujeres).</p> 
        
        <p class="h3subMenu">2.- Donar es un acto con el que se nace</p>
        <p>Muchos de nosotros experimentamos un sentido de filantropía durante nuestra niñez temprana. Un estudio realizado en la Universidad de Columbia Británica, demostró que bebés de uno o dos años, mostraron mayor expresión de felicidad al dar cariño, por ejemplo a un cachorro, que cuando recibían cariño.</p>
        <p>Estudiosos de la generosidad en niños, como Michael Price, de la Universidad de Georgia, y John List, de la Universidad de Chicago, comentan que “hay puntos de referencia morales que determinan nuestra voluntad para compartir con otros”, y explica Price: “desde temprana edad tenemos un deseo para evitar actos que son egoístas, mismos que influyen, en niños, como un patrón de comportamiento para compartir. Esto sugiere que los actos de caridad podrían ser actos o conducta innata”.</p>
        
        <p class="h3subMenu">3.- Donar fortalece nuestra imagen</p>
        <p>Donar no sólo nos hace sentir bien, también nos hace ver bien (y a todos nos preocupa lo que los demás piensen de nosotros). Donar puede hacerte ver más responsable. Eso explica por qué el esfuerzo reconocido, sea por una calificación, una placa con tu nombre o una lista de donaciones, resulta tan gratificante. De acuerdo con Theresa Lloyd, co-autora de <i>Richer Lives: Why Rich People Give,</i> “es una especie de vuelta a nuestro ego y a la importancia de sentirse importante”.</p>
        <p>Las personas escogen verse de cierta manera y fortalecen esa identidad cuando se unen a grupos o comunidades, como es el unirse a su universidad de origen. Para los autores del artículo "Identidad en las donaciones caritativas”, Kessler y Milkman, “hay normas que se asocian a la identidad de cada uno o al sentir de cada uno”, como escriben: “la gente ajustará su comportamiento a las normas, entre más cerca de ellas se encuentren, especialmente cuando les recuerden a su identidad”.</p>
        
        <p class="h3subMenu">4.- Donar produce más donaciones</p>
        <p>Si alguien te da un regalo, te sientes comprometido a dar uno también. El potencial de los donantes puede inspirarse con un regalo de vuelta. Esto no significa que de inmediato el donante deba ser recíproco. Se aconseja que el regalo sea apropiado, y no necesariamente grande, y esperar aproximadamente tres meses para preguntarse por el regalo de vuelta, de esta manera, la persona podrá disfrutar del mismo y desarrollar un sentido de gratitud más profundo.</p>
        <p>Sé creativo. En lugar de mandar un regalo obvio, vincula tu regalo a tu misión y conecta a los donantes con el resultado final de su donación: puede ser una nota personalizada de algún beneficiado o un video de los voluntarios. El intercambio de un regalo no requiere ser caro, sólo necesita ser sincero.</p>
        
        <p class="h3subMenu">5.- Porque hombres y mujeres realmente son de planetas diferentes</p>
        <p>Las mujeres no sólo hacen más donaciones que los hombres, sino que sus donaciones son más altas que la de los hombres. La mujer soltera, a cargo de su hogar, realiza más actos de caridad que el hombre soltero, a cargo de su hogar. Las mujeres tienden a ser más empáticas y altruistas, mientras que los hombres realizan donaciones si consideran que el programa o proyecto al que se destina la donación es una inversión sólida.</p>
        <p>En un estudio publicado por la revista <i>Current Biology</i>, se mostró que los hombres donaron “cuatro veces más dinero a una atractiva mujer que recaudaba fondos, como respuesta a la que otro hombre le había hecho”. Los investigadores señalaron que esta ayuda “basada en la competencia”, podría deberse a algún beneficio derivado de un intercambio evolutivo.</p>
        <p>A diferencia de los hombres, las mujeres consideran que el donar o los actos caritativos son una actividad que incrementa y beneficia la salud. A diferencia de la mujer, quien ve en la salud un camino para alcanzar sus metas y sus objetivos, así como su libertad personal y su seguridad, el hombre ve la salud en términos de poder y de prestigio.</p>
        <p>Las cinco razones que hasta aquí se muestran, permiten considerar más motivos al momento de realizar una donación, pues el acto de donar tiene en sí mismo su propio beneficio: ayudar a quien lo necesita. Mediante sus programas, la Fundación UV pone en práctica lo antes dicho: al asistir a un evento, un curso de capacitación (<a href="https://www.fundacionuv.org/que_hacemos/colaboramos/?tipo=2">https://www.fundacionuv.org/que_hacemos/colaboramos/?tipo=2</a>), al rentar una cancha deportiva (<a href="https://www.fundacionuv.org/que_hacemos/fomentamos/?tipo=1">https://www.fundacionuv.org/que_hacemos/fomentamos/?tipo=1</a>), inscribirte en los Centros de Formación Deportiva (<a href="https://www.fundacionuv.org/que_hacemos/fomentamos/?tipo=2">https://www.fundacionuv.org/que_hacemos/fomentamos/?tipo=2</a>), o al apoyar el Programa Becas (<a href="https://www.fundacionuv.org/que_hacemos/becas/?tipo=1">https://www.fundacionuv.org/que_hacemos/becas/?tipo=1</a>), contribuyes a la formación de estudiantes con buen desempeño académico y vulnerabilidad económica, como una de las principales labores de la FUV.</p>
