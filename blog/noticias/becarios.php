        <p>Sobre el mismo reto, Miguel Ángel, de Xalapa, sexto semestre de Licenciatura en Psicología, mencionó que el material de su carrera consiste en antologías, comprar libros y sacar copias, y que en ello destina alrededor de dos mil pesos semestrales, situación ante la cual comentó: “lo que me ha funcionado es sacarle copias a la mitad del libro o sólo a lo que se va a ocupar; trato también de buscar a alguien que haya cursado la materia, le pido prestada la antología y ya sólo le saco copia a lo nuevo que se haya incluido”.</p>
        <p>También para Miguel, el transporte es otro de los retos que hay que superar: “de mi casa a la Facultad de Psicología son 40 minutos en camión y hay que tomar dos camiones; sin embargo, a veces prefiero caminar una de las dos rutas y la otra recorrerla en camión, aunque haga una hora y media de camino; una vez tuve que tomar un taxi para llegar a un examen, y lo hice, pero desacompleté mis gastos para los siguientes días”.</p>
        <p>La alimentación es uno de los retos que más exige estrategias. Para Iram, por ejemplo, es preferible hacer comida en casa, “pues si comes en una fonda, comes poco y al rato te da hambre otra vez”. En cambio, para Juan, “es más caro comprar comida y hacerla en casa, que comprar comida ya hecha, o comer fuera, pues no puedes comprar sólo una rodaja de tomate o un pedazo de jamón, y si compras más y no se acaba lo que compras, se echa a perder”.</p>
        <p>Miriam también aportó al tema y dijo que en muchas ocasiones su comida del día es “una torta de 15 pesos y un agua de 10, y si me salgo de ese presupuesto, pues pongo en riesgo otras áreas de mi vida; a veces la comida de los hospitales sobra y, pues en lugar que la tiren, tenemos acceso a ella”. Iram señaló al respecto que es muy común hacer una sola comida al día, “en ocasiones hasta la misma cena te basta para no desayunar y llegar a la comida”.</p>
        <p>Como se ha podido apreciar, el reto más importante de un estudiante, al no tener cubiertas sus necesidades de base, no puede ser obtener una buena nota, sino que es todavía más: resolverlas sin perder el propósito inicial: la buena calificación; no puede ser sólo resolver lo académico porque necesita resolver necesidades y si sólo resuelve necesidades, en descuido del ámbito académico, será mejor desertar.</p>
        <p>Frente al reto de ser estudiante, los becarios de la FUV, del apoyo que reciben del Programa de Beca, expresan: “te deja respirar”, “significa confianza; sientes esa seguridad de que va a llegar; cuando sabes que tendrás la beca, te sientes más seguro”, “sabes que un día vas a comer un poco más que otros días”, “te permite organización”, “te saca del apuro”.</p>
