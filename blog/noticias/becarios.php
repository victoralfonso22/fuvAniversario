        <h3 class="h3subMenuNota">El reto del estudiante UV</h3>
        <p class="pieNoticiaNota">Junio 2017</p>
        
        
        <p>La vida de un estudiante suele ir acompañada de retos y uno muy importante, si no el más importante, es obtener buenas calificaciones. Sin embargo, cuando los medios que tendrían que impulsarte a luchar por esa meta, se convierten en retos mismos: alimento, hospedaje, transporte o material de la carrera, el reto más importante es ahora superarlos sin que las calificaciones se vean afectadas.</p>
        <p>Así indicaron los becarios de la Fundación UV, Juan Carlos Morales, Miriam Guarneros, Iram Rodolfo Rudecino, Mireya Tetla Tepixtle y Miguel Ángel Fernández, quienes también señalaron que, si el reto más importante de un estudiante es resolver sus necesidades sin descuidar sus labores académicas (y no sólo resolver sus necesidades) es porque, qué caso tendría resolverlas si cuando lo haces descuidas el propósito inicial: el académico.</p>
        <p>Para Juan Carlos, de Comitán, Chiapas, sexto semestre de Licenciatura en Químico Farmacéutico Biólogo, una de las dificultades como estudiante es compartir el lugar en donde vives: “a veces la disponibilidad y la repartición de gastos se complica: los servicios, la comida, la limpieza; a veces no se compran artículos ni para la casa ni para uso personal”.</p>
        <p>“El gasto más fuerte -continúa Juan Carlos- es el gas, pues no sabes cuándo se va a terminar. Estás con los gastos justos y de repente se acaba, y son 300 pesos; un gasto fuerte que hay que sacar, considerando, también, todos los otros gastos. Llegas a clase pensando: de dónde voy a sacar ese dinero”.</p> 
        
