        <h3 class="h3subMenuNota">Programa Administración de Proyectos: una trayectoria con impacto social</h3>
        <p class="pieNoticiaNota">Marzo de 2017</p>
        <br />
    
        <p>Si uno de los principales objetivos de la Fundación UV es contribuir al desarrollo y al fortalecimiento del vínculo entre universidad y sociedad, el impacto social que el Programa Administración de Proyectos tiene, permite que la Universidad Veracruzana amplíe el desarrollo económico, cultural y sustentable de su entorno.</p>
        
        <p>Con motivo del 25 aniversario de la Fundación UV, a continuación se presenta un recuento de los distintos proyectos que a través de la historia de la Fundación, el Programa Administración de Proyectos ha realizado. </p>
        <p class="h3subMenu">Antecedentes del programa (1992-1997)</p> 
        <p>En los inicios de la Fundación, los programas Proyectos de vinculación con sectores productivos e Investigación, permitieron un acercamiento entre universidad y sociedad, además de sentar las bases de lo que más adelante sería una labor central de la Fundación, la Administración de Proyectos.</p>
        <p>Sobresale el proyecto Quiahuiztlan–Villa Rica, que consistió en una restauración arqueológica de dicha región, así como el vínculo con el Banco Interamericano de Desarrollo (BID), mediante el cual la UV prestó servicios de la mano de investigadores especializados en las áreas de ecología y planeación institucional. Sobresale también el convenio con el Centro de Genética Forestal, el cual tuvo como objetivos crear un vivero que contribuiría a la reforestación de la zona. Este convenio permitió el acceso a modelos de financiamiento educativo, así como al intercambio de proyectos.</p>
        
         <p class="h3subMenu">Crecimiento del programa (1998-2010)</p> 
        <p>En este periodo, el Programa Administración de Proyectos permitió que la Fundación obtuviera fondos, lo cual fue resultado de una mayor experiencia en el rubro administrativo, así como de la transparencia con que condujo sus recursos. Ejemplo de ello es que en 2000, obtuvo de la SHCP, la renovación del permiso como donataria autorizada para recibir donativos deducibles de impuestos sobre la renta. </p>
        <p>Los proyectos que sobresalen en este periodo son, en 2001, la “Casa de la Universidad centro de desarrollo comunitario Vecinos de El Manglar”. En 2002, el programa “Educación comunitaria y programas compensatorios”, el cual se desarrolló en siete estados de la república. En 2003, el XIII Congreso Bienal de la Organización Universitaria Interamericana (OUI), y el programa de Reforestación del Campus CAD, el cual dedicó una investigación a la restauración ecológica y al rescate de cuerpos de agua. En 2005, el Programa Universitario de Apoyo al Empleo (PUAE), que logró vincular comunidad universitaria con sectores productivos, mediante talleres de creatividad y liderazgo, así como mediante consultorías de negocios familiares.</p>
        <p>En 2006, se creó el Plan de Negocios para el Instituto de Medicina Forense, que implementó servicios de capacitación y actividades de comercialización, como son servicios funerarios (velación, cajas, cremación, etc.), y servicios en materia forense. Y el programa de Reforestación y Área Verdes, que mantuvo un monitoreo de macrohongos, del cual surgió un artículo en donde se documentó la riqueza de especies que hasta ese entonces habían proliferado en el Campus. Es importante mencionar que la acreditación, en 2008, que la Fundación recibe del Centro Mexicano para la Filantropía (CEMEFI), es reflejo del crecimiento operativo e institucional en los años que anteriormente se han señalado.</p>
        
                 <p class="h3subMenu">Proyección del programa (2011-en curso)</p> 
                 <p>Tras una reforma a su estatuto, en 2011, la Fundación logró obtener el permiso que le autoriza como donataria internacional, además del registro en la Red del Consejo Organizaciones Civiles del Estado de Veracruz, eventos que permitieron una apertura respecto los posibles donatarios que la Fundación podría procurar. El Programa Administración de Proyectos, retoma esta apertura y comienza a ampliar los vínculos entre universidad y sociedad.</p>
<div class="div1Notas">
<p>Sobresale en 2012, el proyecto “Manejo integrado de roedores en agrosistemas cañeros”, de la Facultad de Ciencias Biológicas y Agropecuarias de Peñuela, Campus Córdoba- Orizaba; la adquisición de derechos para ser depositaria del banco de datos del Centro de Relaciones Internacionales (CERI), con lo cual se tuvo acceso a convocatorias publicadas por organizaciones filantrópicas; así como el proyecto “Renovación de equipo de radioterapia”, el cual buscó conseguir fondos que permitieran renovar el equipo de radioterapia del Instituto de Investigaciones Médico Biológicas (IIMB), dicho proyecto incluía, también, adquirir un acelerador lineal. </p>

