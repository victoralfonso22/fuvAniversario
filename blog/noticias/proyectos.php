        <p>En 2014, un convenio con Sagarpa para desarrollar con el apoyo del Instituto de Ciencias Marinas y Pesquerías de la UV, el “Proyecto integral de ordenamiento para las pesquerías de Túnidos con Palangre, Camarón, Róbalo, Lisa-Lebrancha y Tiburones rayas del Golfo de México”, el cual consiste en la impartición de nueve talleres de trabajo en los estados costeros del Golfo de México y Mar del Caribe.</p>
        <p>En 2015, se implementó el proyecto de encuestas de medición de impacto para el programa de prevención social del delito con participación ciudadana de Veracruz, y el proyecto para la realización de pruebas de efectividad biológica de plaguicidas, pláticas y/o conferencias técnicas, análisis de laboratorio y diagnóstico de enfermedades con la Facultad de Ciencias Biológicas Agropecuarias.</p>
        <p>No obstante que todas las actividades anteriores han contado con una administración y vinculación, y que tienen un impacto social claro, el proyecto que, desde el 2016 al año actual se desarrolla bajo el nombre de “Urgencia de Equipo-Urgencia de Vida” y que se viene gestando desde el 2012 con el IIMB, equivale a una proyección distinta. En éste se desarrollan labores no sólo de vinculación y administración, sino, también, de gestión, esto es, resulta pionero dentro del programa Administración de Proyectos.</p>
        <p>Dicho proyecto albergará un servicio integral de radioterapia a través de un acelerador y un tomógrafo, los cuales estarán colocados dentro del Instituto de Investigaciones Médico-Biológicas de la UV. Este gran proyecto podrá comercializar sus servicios en este año, 2017. Con el acelerador y el tomógrafo se podrá ofrecer, anualmente, 17 mil 500 sesiones de radioterapia, 450 simulaciones de tratamientos y 800 tomografías. El impacto social y las posibilidades de gestión en años próximos, hacen de este proyecto, un proyecto de vanguardia para la Fundación.</p>
        <p>Como antes se ha comentado, el Programa Administración de Proyectos de la FUV, permite que la UV tenga un impacto social, y al permitir esto, la Fundación cumple con uno de sus principales objetivos: contribuir al fortalecimiento y desarrollo del vínculo entre universidad y sociedad.</p>
      
