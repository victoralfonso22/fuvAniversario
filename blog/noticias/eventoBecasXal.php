        </p>
        <p>Este martes 23 de mayo, en el vestíbulo de Tlaqná, Centro Cultural se realizó la “10ª Entrega de Becas Región Xalapa”, correspondiente al periodo febrero–julio 2017, el cual estuvo encabezado por la Dra. Sara Ladrón de Guevara, Rectora de la UV, el Ing. Jesús Antonio López Rosas, Vicepresidente de la Junta Directiva y el Ing. Manuel Cienfuegos García, Presidente del Comité Regional de Becas Xalapa.</p>
        <p>Ante la presencia de autoridades de la máxima casa de estudios del estado de Veracruz, miembros de la Fundación UV, A.C., así como padrinos y benefactores que participan en el Programa de Becas, en sus campañas Adopta un becario y Siempre Universitarios, la becaria Eva Marianne Martínez Fuentes, estudiante de la carrera de Médico cirujano, campus Xalapa, en nombre de los becarios, dijo: “desde el 2012 me convertí en parte de esta gran fundación y al mismo tiempo me hice la promesa de luchar para llenar a mis padrinos de orgullo, al saber que por ellos llegué hasta donde estoy”.</p> 
        
        <div class="div1Notas">
            <p>“Tras 5 años en la facultad –continuó Eva Marianne- pude obtener mención honorífica al presentar mi tesis y pude hacer que los resultados de ésta fueran publicados en una revista. También obtuve reconocimiento en el hospital en el que trabajé un año como interna; esto se los platico para que los padrinos se percaten del gran impacto que puede hacer el pequeño impulso que ustedes nos dan.”</p>
            <p>Por su parte, en nombre de los benefactores de la Fundación UV, el Dr. Ragueb Chaín Revuelta destacó en su discurso el privilegio de ser donante y el poder retribuir el apoyo que durante sus estudios de posgrado él recibió como becario. Invitó, asimismo, a que más personas se unan como donantes de la Fundación UV, de la cual dijo: “resalto la sencillez y la facilidad que dicha asociación otorga mediante sus procesos para que nos convirtamos en donantes”.</p>
            <p>A propósito de la “10ª Entrega de Becas Región Xalapa”, la Rectora de la Universidad Veracruzana, la Dra. Sara Ladrón de Guevara comentó que en su apoyo a la universidad pública, la Fundación UV se une a la iniciativa privada para hacer de la educación la mejor inversión, y dijo: “así como el testimonio de Eva Marianne, cada becario es una historia, una familia que la Fundación UV apoya”.</p>
            <p>Desde su creación, en agosto de 2012, el Comité Regional de Becas Xalapa ha otorgado, a la fecha, mediante 156 benefactores, un total de 906 becas, 327 beneficiados: 218 mujeres y 109 hombres. Tan sólo para el periodo actual, que corresponde a febrero-julio 2017, se registra la entrega de 90 becas, mediante 70 benefactores, a 55 mujeres y 35 hombres, así como un total de 32 programas educativos apoyados.</p>
            
        
