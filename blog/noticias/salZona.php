        <h3 class="h3subMenuNota">Fundación UV invita a la tercera edición de SalZona</h3>
        <p class="pieNoticiaNota">Junio 2017</p>
        <p class="pieNoticiaNota" style="margin-top: -2.5em;">Redacción Universo</p>
        
        
        <p>La Fundación de la Universidad Veracruzana (FUV) realizará la tercera edición de SalZona 2017, el 1 de julio a partir de las 19:00 horas en el Gimnasio Universitario del Campus para la Cultura, las Artes y el Deporte (Campus CAD).</p>
        <p>El Ing. Facundo Enrique Pacheco Rojas, Director Ejecutivo de la Fundación UV, compartió que este evento tendrá una duración aproximada de cinco horas, con posibilidad de extenderse; el ambiente será completamente familiar y los asistentes podrán escuchar la música y bailar, en lo que, calificó, será una auténtica fiesta para los sentidos.</p>
        <p>Pacheco Rojas comentó que quienes estarán en el escenario son la orquesta Xalli Big Band de la UV, la cual alternará su presentación con la agrupación musical Mayabe y el salsero venezolano J.J. Ruiz.“Con este evento se espera vincular a estos grupos musicales con la población en general, la cual podrá disfrutar de canciones de diferentes versiones y épocas.”</p>
        <p>Aseguró que este festival es ya una tradición en la ciudad y que la gente lo espera año con año; relató que la asistencia a la primera y segunda ediciones fue de mil y mil 100 personas, respectivamente, por lo que en esta ocasión se espera superar tal cifra.</p> 
        
        <div class="div1Notas">
            <p>Añadió que las instalaciones del Gimnasio Universitario “Nido del Halcón” del Campus CAD, cuentan con una gran capacidad, ya que en las canchas del mismo pueden bailar hasta 400 parejas simultáneamente; además, SalZona contará con servicio de reservación de mesas para cuatro y 10 personas.</p>
            
           
            
        
