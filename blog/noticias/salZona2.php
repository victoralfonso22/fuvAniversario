            <h3 class="h3subMenuNota">JJ Ruiz</h3>
            <p>Originario de Venezuela, cuenta con una amplia trayectoria musical; en 2013, obtuvo disco de oro con el tema “La salsa de los grandes 2”, en 2016, tuvo la fortuna de participar en el tributo latino a Michael Jackson (<i>“Unity: The Latin Tribute to Michael Jackson”</i>), del que Tony Succar fue productor; además de interpretar en género de salsa, éxitos como “Tú me vuelves loco” y “A dónde vamos a parar”.</p>            
            <p>Aunque en años previos pudo grabar diversos materiales discográficos, con otros salseros, no fue hasta 2016 que JJ Ruiz pudo tener en sus manos su primera discografía, la cual denominó “Mi esencia”, y en el que se recopilan 11 temas de su autoría, entre ellos, “Mulata”.</p>
