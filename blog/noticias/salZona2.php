        <h3 class="h3subMenuNota">Cartelera SalZona 2017: gran festejo al ritmo de la salsa</h3>
        <p class="pieNoticiaNota">Junio 2017</p>
        <!--<p class="pieNoticiaNota" style="margin-top: -2.5em;">Redacción Universo</p>-->
        
        <p>En atención a la cartelera de SalZona 2017, a realizarse el próximo 1ro. de julio, a partir de las 19:00 hrs., en el Gimnasio Universitario “Nido del Halcón” del Campus CAD, la tercera edición de SalZona resulta ser un gran festejo del ritmo caribeño y latino, al ritmo de la salsa; cada orquesta, intérprete o cantautor, y academias de baile participantes, son una muestra del cariño y el cultivo que se tiene en el estado de Veracruz, por este género musical.</p>
        
        <h3 class="h3subMenuNota">Xalli Big Band</h3>
        <p>Creada en 2009, con el respaldo de la UV, es actualmente uno de los grupos más representativos de la UV; tras siete años de trayectoria, destacan entre sus presentaciones, las del Palacio de Minería de la Ciudad de México, la Sala Emilio Carballido del Teatro del Estado, el World Trade Center del Puerto de Veracruz, y el Festival Internacional Tajín, en Papantla, Veracruz.</p>
        <p>Las dos presentaciones que tienen programadas para este 1ro. de julio, son su primera aparición en SalZona; parte del repertorio que tienen contemplado, es: canciones de la Sonora Matancera, como “Ave María, Lola”, canciones de Willy Chirino, como “Rumbera”; una versión muy particular de “Caballo viejo”, un popurrí con los grandes éxitos de Oscar D' León, algunos clásicos cubanos, entre otros.</p>
        <p>Una característica de la orquesta Xalli Big Band es que su sonido corresponde al estilo de las grandes bandas de los años cincuenta; además, los arreglos de las piezas musicales que tocan, son únicas y no sólo versiones o <i>covers</i> de las originales. </p>
        <p>Otro aspecto a resaltar es que no es común escuchar grandes bandas que toquen salsa: poder tocar salsa con 20 metales (en lugar de cinco), cuatro trompetas, cuatro trombones, cuatro saxofones, permite una audición más rica y una variedad de arreglos posibles, para grandes piezas de distintas épocas. </p>
            
        
