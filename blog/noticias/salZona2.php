        <p>JJ Ruiz, quien gusta de llevar a sus dos hijos a las presentaciones, radica actualmente en el estado de Veracruz, lugar del que resalta el gusto por la salsa, y gracias al cual, ha podido desarrollar su pasión por el género. SalZona 2017 será su primera aparición por lo que se tiene gran expectativa al respecto.</p>            

        <h3 class="h3subMenuNota">Mayabe</h3>
        <p>De origen xalapeño, creado en 2009, por iniciativa de Aram Jesus y su hermano Isaac Orlando, cuenta con 7 años de trayectoria, periodo en el que ha podido consolidarse, tanto en la ciudad de Xalapa como en el resto de la entidad veracruzana, como un grupo versátil que gusta compartir con su público, su alegría por el género.</p>
        <p>Mayabe ha tenido también la oportunidad de compartir escenario con bandas como El Recodo, la orquesta Guayacán, de Colombia, así como con el cantante neoyorquino y compositor de salsa, Ismael Ruiz Hernández, mejor conocido como “Maelo” Ruiz.</p>
        <p>En lo que será su tercera aparición en SalZona, Mayabe garantiza, este 1ro. de julio, una propuesta divertida y comprometida con el género de la salsa, cualidades que les han acompañado en sus 7 años de vida.</p>

        <h3 class="h3subMenuNota">Academias de Salsa de Xalapa</h3>
        <p>Por su parte, cinco academias de baile, reconocidas por su profesionalismo en el ritmo caribeño y latino, sumarán a SalZona 2017, mediante demostraciones en su cartelera, los elementos dancísticos con los que el género de la salsa se expresa.</p>
        <p>La academia La Gua-Gua, activa desde 2015, cuyo nombre refiere en Cuba, al medio de transporte, contará con dos demostraciones, mismas que expondrán por qué el baile y la música son los mejores medios para transportar y transmitir emociones; este año será su segunda participación en SalZona.</p>
        <p>La academia Pasión y Son, ofrecerá tres demostraciones; cuenta con 5 años de actividad y, como su nombre lo indica, su motivación principal es expresar la pasión que sienten por los ritmos caribeños y latinos; SalZona 2017 será también su segunda participación por lo que este 1ro. de julio, de nuevo podrán contagiar al público asistente.</p>
        <p>Academia Ponte N'Klabe; primera academia de baile popular cubano, en Xalapa, y que, sin importar edad, enseña rumba, son, salsa, entre otros, tanto a niños de 5 años, como a adultos de 83. En SalZona 2017, contará con dos demostraciones, mismas que desarrollará al estilo cubano “baile de casino”. La academia Ponte N'Klabe, en lo que será su primera experiencia en SalZona, espera entusiasta el próximo 1ro. de julio.</p>
        <p>La academia Son Chumbe, creada en 2014, busca divertir a su público y pasar un buen momento; su nombre refleja la importancia que para ellos tienen las raíces populares del son jarocho; con las dos demostraciones que tienen programadas, cumplirán su tercera aparición en SalZona, evento que para ellos, es ya una tradición. A la cartelera anterior se une la academia Talleres de Salsa Xalapa, la cual contará, este 1ro. de julio, con una demostración.</p>

