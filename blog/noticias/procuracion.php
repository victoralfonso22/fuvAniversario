        <p>En 1999, se llevó a cabo el estreno nacional de la película “El coronel no tiene quien le escriba”, en el castillo de San Juan de Ulúa del Puerto de Veracruz, película que fue dirigida por Arturo Ripstein y que se basó en la novela homónima del escritor colombiano Gabriel García Márquez. En 2002, se estrenó la película “Otilia Rauda”, basada en la novela del veracruzano Sergio Galindo y que le valió, en 1986, el Premio Xavier Villaurrutia; la Fundación UV apoyó para la producción de la película, cuyo estreno permitió promover donativos en beneficio de la Fundación.</p>
        




<p>En 2003, se creó el proyecto “Jarocho”, grupo artístico de música y danzas de Veracruz, para apoyar los programas de la Fundación, mientras que en 2005, junto con la Universidad Veracruzana y Ediciones Pentagrama se participó en la coproducción de “Tres grandes de Tlacotalpan”, disco acompañado de un video y la presentación premier. Sobresale en 2006, el concierto de Julieta Venegas y Pablo Milanés, así como el documental “Ernesto Cardenal… pescador de sueños”, que muestra la obra y escultura de este poeta, realizada en el archipiélago Solentiname, Nicaragua.</p>
<p>Para 2014, aunque con la creatividad de los primeros años, los eventos dirigidos a la obtención de fondos adquirían mayor organización, como es la Cata de vinos y el concierto “Navidad en Latinoamérica y otros Rincones del Mundo”, el cual fue interpretado por la Orquesta Universitaria de Música Popular (OUMP) y la agrupación Tlen-Huicani. En 2015 y 2016, los eventos fueron “Cara a Cara. Un viaje al inframundo prehispánico”, espectáculo que reunió a más de 100 artistas, entre ellos estudiantes y egresados de las Facultades de Teatro, Danza y Música, además del Ballet Folklórico de la UV, la Camerata Coral de la Facultad de Música, la Opera Juvenil de Xalapa, el Grupo Talacha Teatro. Salzona UV, edición 2015 y 2016, fueron eventos de música y baile dirigido a los amantes de este ritmo, los cuáles reunieron en el Gimnasio Universitario a más de mil 100 personas para bailar salsa en compañía de la Orquesta de Salsa de la UV y las orquestas Mayabe y XL de Cuba. </p>


        
        <p>Si bien todos los programas de la Fundación, incluyen estrategias para obtener fondos que permiten su fortalecimiento, en el programa Eventos para la Procuración de Fondos, la Fundación tiene uno que se dedica de manera exclusiva a ello sin descuidar su impacto social. La importancia de tener un programa que se dedique de manera exclusiva a la procuración de fondos es que los demás programas de la Fundación no dejen de verse fortalecidos por las actividades que este programa realice. </p>
      
