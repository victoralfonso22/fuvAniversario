    <div class="div1Notas">
        <p>Debido a que la Fundación UV, primera asociación civil de una universidad pública en México, nació sin fondos en sus arcas, la realización de eventos para procurarlos, desde el momento mismo en que nació, se convirtió en un programa que permitiría el fortalecimiento de los demás programas. Es por esto que, en el marco de los 25 años de la Fundación, el Programa Eventos para la Procuración de Fondos, adquiere mayor relevancia. A continuación se presentan algunas actividades para la obtención de fondos de la Fundación, que mayor impacto han tenido en el transcurso de su historia.</p>
        
        <p>En sus primeros años, sobresalen actividades que van desde la venta de libros, cassettes, discos, revistas, así como la producción de obras de teatro o exposiciones de obras de arte y antigüedades, hasta un “Concierto de gala” en el Museo de Ciencia y Tecnología.</p></div>
