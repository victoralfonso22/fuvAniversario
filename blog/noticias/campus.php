        <p>Aunque las actividades en el Campus para la Cultura, las Artes y el Deporte (CCAD), están bien definidas: si son deportivas, pueden apreciarse a través de los Centros de Formación Deportiva Infantil y Juvenil, si son culturales, pueden encontrarse en la Sala de Conciertos Tlaqná o en la Unidad de Servicios Bibliotecarios y de Información (USBI), y si son recreativas, pueden buscarse en el Lago Mayor, las razones por las que los usuarios visitan dicha instalación, permiten que su atractivo se vea enriquecido.</p>
        <p >Es el caso de Erick Campechano, Dayana Sandoval y Naomi Santes, de segundo semestre de Negocios Internacionales en la UV, quienes señalaron que uno de los principales beneficios al visitar el Campus CAD es la diversidad de actividades que sus instalaciones ofrecen. Y comentaron: “nos gusta que podemos jugar en canchas en buen estado y hacer deporte. Lo que más nos gusta es jugar voleibol y escuchar conferencias”.</p> 
        <div class="div1Notas">
            
