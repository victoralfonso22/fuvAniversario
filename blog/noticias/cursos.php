        <p>En la región Veracruz-Boca del Río, “Crea e innova” tuvo su primera ejecución, el pasado 20 de abril; pero se tienen contemplados para el resto de este año, alrededor de 40 cursos, entre 600 y 800 personas capacitadas. La meta de la Fundación UV es capacitar 2 mil 500 personas, en todo el estado de Veracruz.</p>
        <p>Los cursos para el presente año, corresponden a una primera fase de sensibilización, en palabras del Ing. Facundo Pacheco, Director de la Fundación UV, “fase en la que se muestra la importancia de la metodología, la complejidad de trabajar en equipo y el estar listos para resolver las problemáticas. </p>
        <p>Una segunda fase, contemplada para 2018, consistirá en dirigir dicha metodología a algo más específico: trabajar con estudiantes y académicos para que apliquen el curso a clases, trabajos recepcionales o la elaboración de tesis”. Los cursos de “Crea e innova”, podrán ser de dos tipos: homogéneos o heterogéneos. Si es heterogéneo, los grupos serán multidisciplinarios y permitirán que se analicen problemas desde diferentes enfoques: humanista, técnico, artístico, médico. Los grupos heterogéneos plantean también la posibilidad de desarrollar proyectos de tutorías entre maestros y estudiantes.</p>
        
                
                <p>La alianza que tiene Fundación UV con Sinequanon, empresa mexicana que es especialista en desarrollo organizacional, permite contar con instructores certificados por LEGO, así como con el material didáctico requerido para la elaboración del curso; también permite que el costo sea accesible, pues, normalmente, oscila entre 2 mil y 3 mil pesos por persona.</p>
                <p>El costo de “Crea e innova”, es: 300 pesos, para estudiantes; 500 pesos para comunidad universitaria (administrativos, académicos, investigadores, sindicalizados); y mil pesos para público en general. Al final del curso se entrega un diploma con la certificación de LEGO. </p>
                <p>Los cursos para el 2017, pueden calendarizarse en acuerdo con los interesados. La plataforma con la que se cuenta permite que se lleven a cabo dos cursos diarios, uno en la mañana y otro en la tarde, entre semana o fines de semana. Para mayores informes respecto a la realización de cursos, comunicarse al correo: <u>capacitacion1@fundacionuv.org</u> o a los teléfonos: (228) 841 4580 y 818 4134.</p>
                
