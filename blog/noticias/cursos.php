            <p class="h3subMenu">“Crea e innova”</p>
            <p>Este curso está enfocado a desarrollar la creatividad e innovación de sus participantes. A través de <i>“Serious Play”</i>, técnica de vanguardia de la empresa LEGO, “Crea e innova” permite que sus participantes proyecten su talento, sus capacidades de liderazgo y el trabajo en equipo, en modelos 3D. </p>
            <p><i>“Serious Play”</i> surgió en 2002, cuando la industria LEGO decidió ramificar sus productos ante el incremento de videojuegos en el mercado; <i>“Serious Play”</i> se dirigió, entonces, al sector empresarial como un método que permite alinear cultura organizacional y los objetivos del sector. La UV es la primera universidad pública en México, en utilizar dicha técnica.</p>
        
