        <h3 class="h3subMenuNota">“Taller de Manejo de Fauna Venenosa”: una oportunidad para acercarse a la especie</h3>
        <p class="pieNoticiaNota">Abril de 2017</p>
        <br />
        
        
        <p>Con el propósito de aclarar los mitos que acompañan a la llamada fauna venenosa, el “Taller de Manejo de Fauna Venenosa” consistió en una oportunidad para que sus asistentes, a través de técnicas actuales basadas en el comportamiento de cada especie, pudieran acercarse a reptiles y serpientes.</p>
        <p >Para Pablo Cobos Mejía, encargado del taller y responsable de la Unidad de Manejo de Fauna del Campus CAD, “el mundo de animales venenosos tiene muy mala información, lo cual conduce a crear mitos. El taller, entonces, se realizó para despejar dudas de la mano de conferencistas altamente calificados y con base en un contenido temático actualizado”.</p> 
        <div class="div1Notas">
            
            <p>En palabras de Elías de Jesús Carmona, encargado de impartir la conferencia Comportamiento de los reptiles para un manejo adecuado tanto en campo como en cautiverio, “un buen manejo requiere tener en cuenta las etapas del animal: si es cría, juvenil o adulto. Las crías, por ejemplo, son más nerviosas, más rápidas y más ágiles por lo que la manipulación es un poco más agresiva. Los juveniles son más fuertes por lo que tenemos que ser más rápidos en la manipulación, mientras que cuando son adultos, la manipulación se complica más y tienen que intervenir, a veces, dos personas”.</p>
        <p>En su experiencia, Isadín Baldera Rivera, estudiante de Licenciatura en Administración de Fauna Silvestre, de la Escuela Superior de Medicina Veterinaria de Puebla, el taller permitió ampliar su información acerca del manejo de reptiles y de las medidas de seguridad para su tratamiento, “desde cómo sujetarlos, hasta cómo medicarlos”, indicó. Por su parte, Óscar Samael, estudiante de la misma licenciatura, dijo que “debido a que el aspecto teórico del taller es muy completo, he podido complementar mis conocimientos prácticos en el manejo de fauna”. Mientras que José Osorio, estudiante de Veterinaria, expresó que “lo que en primera instancia me atrajo al taller fueron las nuevas técnicas y los tipos de manejo de fauna, por ejemplo, las entubaciones”.</p>
