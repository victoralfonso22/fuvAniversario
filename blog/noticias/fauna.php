        <p>Para Elías de Jesús, la respuesta al taller, tanto por estudiantes, gente de campo o personas de distintas instituciones, se debe a que su información es actual: “en el campo pre hospitalario, por ejemplo, solía ocuparse la succión de la herida, como en las películas, o la aplicación de torniquetes; sin embargo, se comprobó que estas técnicas no tienen un efecto en el paciente. Lo que hacemos nosotros es no succionar y no aplicar torniquetes porque la única vía para salvar nuestras vidas es el antídoto: no hay porcentaje de muertes después de aplicación de antivenenos”.</p>
        <p>De acuerdo con Cobos Mejía, los asistentes “pudieron irse con la mente más abierta acerca de la importancia de saber que la fauna venenosa son animales que nos causan más beneficios que perjuicios, además de que logró informarse acerca de qué hacer en caso de envenenamiento”.</p>
        <p>Los temas impartidos fueron: biodiversidad, comportamiento y bienestar animal, coralillos no venenosos y arácnidos venenosos, medicina preventiva y primeros auxilios. Mientras que se manejaron especies como: <i>Crotalus atrox</i> o cascabel diamantada, <i>Crotalus molossus</i> o cascabel de cola negra, <i>Crotalus simus</i> o cascabel tropical, <i>Bothrops asper</i> o nauyaca, víbora terciopelo, Boa constrictor o mazacuate, <i>Iguana iguana</i> o iguana verde.</p>
