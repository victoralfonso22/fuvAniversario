        <h3 class="h3subMenuNota">Razones para NO donar</h3>
        <p class="pieNoticiaNota">Julio 2017</p>
        <!--<p class="pieNoticiaNota" style="margin-top: -2.5em;">Redacción Universo</p>-->
        
        <p>La acción de donar suele verse, en el mejor de los casos, como un acto que si bien no nos quita, tampoco nos beneficia; en “5 razones para donar”, publicado en este mismo blog (<a href="https://www.fundacionuv.org/blog/noticias/5Razones.php">https://www.fundacionuv.org/blog/noticias/5Razones.php</a>), se ofrecieron algunas razones que podrían mostrar lo contrario. En esta ocasión, retomamos el estudio de especialistas en el tema de donación<span style="vertical-align: super">1</span>, para exponer algunas razones por las que suele decidirse no donar.</p>        
                
        <h3 class="h3subMenuNota">1. Falta de confianza</h3>
        <p>La falta de confianza afecta no sólo la probabilidad de las donaciones, sino, también, la cantidad de las mismas. De acuerdo con investigadores de la Universidad de Columbia, en Nueva York, cuando se trata de hacer una donación, la falta de confianza vence muchos de los factores que podrían considerarse benéficos para el acto de donar, como es la cantidad a donar, pues aunque sea un donativo pequeño, la falta de confianza podría evitar que se realice, o el género: aunque la campaña de donación plantee alguna afinidad entre mujeres u hombres, ésta podría no llevarse a cabo.</p>
            
        <h3 class="h3subMenuNota">2. Comunicación fallida</h3>
        <p>La compañía de software, Abila, realizó una encuesta electrónica para medir la fidelidad de sus donantes; ésta mostró que el 75 por ciento de los encuestados, rechazó la propuesta de donación debido a una comunicación fallida, la cual incluye un contenido pobre o vago, boletines genéricos, nombres mal escritos o material inapropiado para la edad, como es solicitar donativos a personas que no tienen esa capacidad. El 50 por ciento de los encuestados, prefirieron recibir información de manera quincenal o mensual; más de la mitad, recibir información al menos una vez por mes; y la mayoría, recibir información de manera puntual y concreta.</p>
            
        <h3 class="h3subMenuNota">3. Que sólo se pida dinero</h3>
        <p>Aunque solicitar donaciones es una de las funciones principales de toda instancia donataria, que sólo se pida dinero es un error fatal, especialmente si el grupo de personas al que se dirige la solicitud, resulta económicamente vulnerable. En su lugar, Una Osili, del Colegio de Filantropía de la Universidad de Indiana, sugiere fomentar el voluntariado y la participación sin restricciones en los distintos eventos que se realicen; por ejemplo, en la encuesta de Abila, arriba mencionada, el 75 por ciento de los encuestados que fueron voluntariados, estuvieron más dispuestos a donar. </p>
            
        <h3 class="h3subMenuNota">4. Mal momento</h3>
        <p>Con frecuencia, solicitar un donativo corre el riesgo de hacerse en un mal momento, esto se debe, según Jeff Hill, de la Universidad de North Carolina, a un desconocimiento de la persona o del grupo de personas al que se dirige dicha solicitud. Si en su lugar se tiene buen entendimiento de la relación entre donatario y donante, Hill asegura, podrá indagarse acerca de la razón por la cual se ha decidido no se donar, y si en ese momento no ha optado por no donar, mostrarse comprensivo en la solicitud podría,  en un futuro, traerlo de vuelta.</p>
        <p>Las anteriores razones permiten conocer lo que usualmente nos motiva a decidir por no donar; sin embargo, tenerlas en cuenta podría también indicarnos si nos encontramos en una situación que plantea un buen escenario para donar. </p>
