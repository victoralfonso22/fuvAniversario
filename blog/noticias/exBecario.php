        <h3 class="h3subMenuNota">Exbecario Víctor Pérez: impulso al potencial universitario</h3>
        <p class="pieNoticiaNota">Junio 2017</p>
        
        
        <p>Los becarios se admiran de la confianza de sus benefactores y buscan siempre responder a ello con el mejor esfuerzo posible; no obstante, el benefactor no solo deposita su confianza sin un conocimiento pleno de la otra persona, sino que además lo hace sin conocer las consecuencias. Puesto que no se sabe en qué se transformará su ayuda, el Programa Becas de la Fundación UV es un impulso al potencial universitario.</p>
        <p>Ejemplo de ello es Víctor Pérez García. La FUV se percató de su potencial, en febrero 2001, y, sin saber consecuencia alguna, convirtió a Víctor en becario durante los últimos tres semestres de su Licenciatura en Matemáticas. Proveniente de El Cedro, comunidad de Papantla, Veracruz, y de origen totonaca, Víctor Pérez actualmente es Coordinador del Doctorado de Matemáticas en la Facultad de Matemáticas de la Universidad Veracruzana.</p>
        <p>Su gusto por las matemáticas surgió desde muy temprana edad, pero no se percató de lo que quería hacer en su vida, hasta su etapa preparatoria, cuando al visitar la Facultad de Matemáticas de la UV, en 1995, con motivo de las olimpiadas mexicanas de matemáticas, dijo: “yo quiero estudiar aquí”.</p>
        <p>Al respecto, comenta: “me gustó la formalidad de las matemáticas. En la preparatoria nos daban clases extra y eso me gustó todavía más. Ya cuando llegué a la Facultad de Matemáticas, empecé a estudiar razonamientos y me llamó mucho la atención el poder encontrar resultados a partir de ciertas reglas”. De entre todos los aspirantes a la carrera, en 1998, Víctor obtuvo el cuarto mejor resultado en el examen de admisión.</p> 
        <p>Tras concluir sus estudios de licenciatura, en auxilio tanto de clases particulares impartidas durante su carrera, así como de becas de la Fundación Corpus Christi de Papantla, el Instituto Nacional Indigenista y la Fundación UV, Víctor continuó sus estudios de maestría y doctorado en el Cimat (Centro de Investigación Matemática).</p>
        <div class="div1Notas">
            <p>Sobre ello, expresa: “en la maestría elegí el área de matemáticas básicas, que son matemáticas abstractas, y después, en el doctorado, me especialicé en el área de análisis matemático, que es lo que trabajo actualmente”.</p>
            
           
            
        
