        <p>En 2010, realizó una estancia post-doctoral en la Universidad Maria Curie-Sklodowska, en Lublin, Polonia, experiencia que describió como sorprendente y que simplemente le resulta impensable si no fuera por la pasión que los números le despertaron. Durante esta estancia logró profundizar en la llamada “teoría del punto fijo” y pudo comenzar a publicar. Actualmente cuenta con nueve publicaciones en revistas internacionales, entre ellas, <i>Journal of Mathematical Analysis and Applications y Journal of Nonliner and Convex Analysis,</i> esta última japonesa.</p>
        <p>La teoría del punto fijo, tema con el que Víctor trabaja hoy día, tiene aplicaciones en distintas áreas, desde ingeniería, economía y física, hasta comunicación. Así lo explica: “la teoría del punto fijo es una rama del análisis matemático que trabaja con funciones. Y, ¿qué es una función? Bueno, supongamos que tienes un objeto o un conjunto de objetos, decir que le aplicas una función a esos objetos, es decir que les haces algo o que los mandas a un lugar. Sin embargo, a veces, hay elementos que no se mueven, a esos elementos se les llama 'puntos fijos'”.</p>
        <p>Para él, entonces, la pérdida de señal digital, y de la información transmitida mediante medios digitales, podría recuperarse mediante la aplicación de la teoría del punto fijo, pues permitiría reconstruir la totalidad de la señal perdida. No obstante, es importante señalar que la aplicación de esta teoría es, todavía, un objetivo a largo plazo y su estudio, como Víctor asegura, se encuentra en fortalecimiento mediante la creación de cuerpos académicos y la realización de congresos en colaboración con grupos de la UNAM y el Cimat.</p>
        <p>Para Víctor, su presente es, en palabras suyas, “un sueño hecho realidad”, el cual es consecuencia, sin duda, tanto de su tenacidad como de su profesionalismo; para la Fundación UV, la realidad actual de Víctor es un ejemplo del potencial que, en cada universitario, debe impulsarse.</p>
