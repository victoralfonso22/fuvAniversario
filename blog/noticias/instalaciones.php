        <h3 class="h3subMenuNota">Administración de Instalaciones: plataforma para el deporte, la cultura y la recreación</h3>
        <p class="pieNoticiaNota">Marzo de 2017</p>
        <br />
        <div class="div1Notas">
        <p>El espacio universitario dedicado al deporte, la cultura y la recreación impulsa la formación integral en el estudiante. La oportunidad de elegir entre distintas actividades: pertenecer a un equipo de fútbol, participar de algún concierto o poder realizar una caminata en una zona segura, son opciones que fortalecen el desarrollo de la comunidad universitaria. En el marco del 25 aniversario de la Fundación UV, se presenta un recuento de algunas de las acciones y estrategias que han posibilitado al programa Administración de Instalaciones, ser, hoy día, una plataforma para la recreación, la cultura y el deporte.</p>        
        <p>En 1998, la Fundación recibió en comodato, de parte de la Universidad Veracruzana, el Campus para la Cultura, las Artes y el Deporte (CCAD), para hacerse responsable de su administración. En 1999, se hace también entrega en comodato de la Unidad de Actividades Deportivas de la Ciudad de Xalapa y del Parque Deportivo de Béisbol de Veracruz. Para el periodo 2000-2001, se habían realizado distintos eventos, entre ellos el torneo de fútbol que organiza la Fundación, en la Unidad Deportiva, y la operación y conservación de las instalaciones deportivas y el área natural, ambos en el CCAD.</p>
