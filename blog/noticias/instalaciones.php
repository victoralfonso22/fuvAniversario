        <p>En 2001, entró en operación el Gimnasio universitario, lo cual llevó a que en 2003, se lograra consolidar el proyecto de Centros  de Formación Deportiva de soccer y de baloncesto, para niños de 4 a 17 años, en donde 365 niños participaron en el primero y 60 en el segundo. En este mismo año inició el programa de Reforestación, que dedicó su investigación a la restauración ecológica y al rescate de cuerpos de agua en el CCAD. Además, para los partidos de los Halcones, equipo profesional de baloncesto que comenzó sus actividades en el CCAD en 2003, se apoyó con vigilancia dentro y fuera del gimnasio.</p>
        




<p>En 2006, se organizó el Primer Campeonato Premier de Fútbol Rápido, categoría libre. Se realizó, también, en el Lago Mayor del CCAD, el espectáculo “Isla Lunagua” del grupo de teatro holandés The Lunatics, el cual logró atraer un total de 6 mil personas. En 2007, en coordinación con la Dirección de Actividades Deportivas de la UV (DADUV), se lanzó el primer torneo femenil de fútbol asociación y categoría libre. En 2008, se celebró el Torneo Nacional Disco en la Lluvia Discolapan 2008, y el UV Fest 2008 “Festival por la Tierra”, en donde hubo muestras artísticas y culturales.</p>
<p>En 2009 se otorgó en comodato el campus de Ixtaczoquitlán con una extensión de casi cincuenta hectáreas por lo que de inmediato se empezaron a evaluar propuestas deportivas, de mantenimiento, así como un plan integral de seguridad y protección civil. En 2012, comenzó a realizarse un curso de verano que hasta el día se lleva a cabo en el CCAD; en lo que respecta a 2012, dicho curso se realizó con 356 niños y 63 personas de apoyo. También se apoyaron las actividades de la Universiada 2012. Y en la Unidad Deportiva-Boca del Río, se llevó a cabo medio maratón de casa Homs, un curso de natación y torneos de fútbol. </p>


        
        <p>Mientras que en 2013, se recibió en comodato “Tlaqná. Centro Cultural” (TCC), 17 mil 23 metros cuadrados de superficie que incluye una sala principal para mil 205 espectadores y una sala anexa para 420 espectadores, distribuidas en 18 mil 755 metros cuadrados de construcción, para que sea la Fundación la que administre, opere y coordine las actividades llevadas a cabo en dicho centro cultural; Tlaqná es sede de la OSX. En este mismo año, el curso de verano realizado en el CCAD, ascendió a 400 niños recibidos, y se llevó a cabo el programa Halcones UV de Fútbol Americano (tochito NFL), así como la escuela de tiro con arco; mientras que en la Unidad Deportiva Boca del Río, se efectuaron cursos de verano en la alberca, natación infantil y juvenil, y la escuela de Fútbol Soccer.</p>
        <p>En 2014 se registró un crecimiento en fútbol soccer, fútbol americano, baloncesto y tenis, para un total de 700 niños, y se identificaron grupos de excelencia que habían formado parte de campeonatos estatales y nacionales. Sobresale en 2015, un curso de entrenadores para certificar a los instructores de las diferentes actividades que se realizan. Se realizó también el Festival Mictlán: dos eventos en el gimnasio y uno en el lago mayor, y las actividades culturales y de recreación: Festival Todos Santos, el concierto OSX en el lago mayor, y el concierto de OSX en el Gimnasio (Queen).</p>
        <p>Ya sea para la recreación del estudiante, la actividad deportiva o para presenciar eventos culturales, el Programa Administración de Instalaciones de la FUV, mediante la operación y el mantenimiento de los espacios que ha recibido en comodato, resulta una plataforma para el desarrollo integral de la comunidad universitaria.  </p>
