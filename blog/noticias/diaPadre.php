        Creo que lo más importante de ser papá se encuentra en el ejemplo, en enseñarles más de la vida y de ellos mismos. Considero que, hoy día, la relación entre padre e hijo consiste más en aprender juntos y no tanto en imponer la educación, como antes se hacía. 
