        Considero que soy un papá comprensivo y tolerante; comprensivo porque me gusta apoyar a mi hijo en lo que a él le gusta y no tratar que sea como yo quiero que sea; y tolerante porque no me gusta que los problemas afecten mi dinámica con él. En lo que a mí respecta, creo que la importancia de ser padre radica en orientar a los hijos, en apoyarlos en lo que ellos decidan y quieran ser en la vida.</p> 
